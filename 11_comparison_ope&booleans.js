//booleans (true, false)
let numberOne = 76;
let numberTwo = "76";
console.log(numberOne >= numberTwo);

// == Vs ===
console.log(numberOne == numberTwo); // == sudhu value check kore, dataType check kore na;

console.log(numberOne === numberTwo); // === eti value and DataType dutai check kore;

// != Vs !==
console.log(numberOne != numberTwo); //only check regardless of operands type;

console.log(numberOne !== numberTwo); // compare both value and type of 2 operands;